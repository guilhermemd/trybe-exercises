function sum(a, b){
  return a + b;
}

console.log(sum(5, 6));

function sub(a, b){
  return a - b;
}

console.log(sub(6, 2));

function mult(a, b) {
  return a * b;
}

console.log(mult(3, 2));

function division(a, b) {
  return a / b;
}

console.log(division(10, 2));

function modulo(a, b) {
  return a % b;
}

console.log(modulo(50, 5));